    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        coneccion b = new coneccion();
        String a=(String) VentanaFactura.combo1.getSelectedItem();
        int cont=0,id_fac,rut;
        rut=Integer.parseInt(a);
        cont=lista_prod.size();
        if(cont>0)
        {
        id_fac=b.ID_ultima_factura()+1;
        b.InsertarFactura(id_fac,rut);
        JOptionPane.showMessageDialog(null, "El ID de factura es : "+id_fac , "Informacion", JOptionPane.PLAIN_MESSAGE);
         
        for(int i=0;i<cont;i++)
        {   String d=(String) lista_prod.get(i);
            int id_pro=b.ID_producto(d);
            b.InsertarItem(id_fac,id_pro);
            
            
        }
         dispose();
        }
        else
        {
        JOptionPane.showMessageDialog(null, "Debe aÃ±adir productos a la factura", "ERROR", JOptionPane.PLAIN_MESSAGE);
        }
        
        
        
       
        
    }//GEN-LAST:event_jButton2ActionPerformed

